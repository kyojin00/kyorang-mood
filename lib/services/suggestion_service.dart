  static final Random _random = Random();

  /// 성향과 기분에 맞는 제안 문구를 하나 반환한다.
  static String pick(SuggestionType persona, int moodLevel) {
    final band = _band(moodLevel);
    final pool = _content[persona]?[band];
    if (pool == null || pool.isEmpty) return _fallback;
    return pool[_random.nextInt(pool.length)];
  }

  static String _band(int level) {
    if (level <= 2) return 'low';
    if (level == 3) return 'mid';
    return 'high';
  }

