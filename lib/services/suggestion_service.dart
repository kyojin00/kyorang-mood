/// 맞춤 제안 서비스 (MVP - 직접 큐레이션).
///
/// 사용자 성향(SuggestionType)과 기분 값(1~5)에 맞춰
/// 미리 작성해 둔 제안 문구 중 하나를 골라 돌려준다.
///
/// 기분은 세 구간으로 나눈다.
/// - low (1~2): 가라앉은 상태 → 위로/다독임
/// - mid (3)   : 보통 → 가벼운 환기
/// - high (4~5): 좋은 상태 → 그 기분을 누리도록
///
/// 추후: 사용 반응 데이터를 반영한 추천 보정 / AI 생성으로 확장.
class SuggestionService {
